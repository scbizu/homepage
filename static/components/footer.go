package components

import (
	"github.com/gopherjs/vecty"
	"github.com/gopherjs/vecty/elem"
	"github.com/gopherjs/vecty/prop"
)

// Footer defines the footer structrue
type Footer struct {
	vecty.Core
}

// Render renders the footer structrue
func (f *Footer) Render() vecty.ComponentOrHTML {
	return elem.Div(
		vecty.Markup(prop.ID("scnace-footer")),
		elem.Footer(
			vecty.Markup(
				vecty.UnsafeHTML(`❤️ Auto-generated By <a href="https://github.com/gopherjs/gopherjs">GopherJS</a> and <a href="https://github.com/gopherjs/vecty">vecty</a>`),
			),
		),
	)
}
